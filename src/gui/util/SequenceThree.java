package gui.util;

public class SequenceThree {
	
    private int ocorrencias;
    private int num1;
    private int num2;
    private int num3;

    public SequenceThree(int ocorrencias, int num1, int num2, int num3) {
        this.ocorrencias = ocorrencias;
        this.num1 = num1;
        this.num2 = num2;
        this.num3 = num3;
    }

    public int getOcorrencias() { return ocorrencias; }
    public int getNum1() { return num1; }
    public int getNum2() { return num2; }
    public int getNum3() { return num3; }
    
}
