package gui.util;

public class SequenceFour {
	
    private int ocorrencias;
    private int num1;
    private int num2;
    private int num3;
    private int num4;

    public SequenceFour(int ocorrencias, int num1, int num2, int num3, int num4) {
        this.ocorrencias = ocorrencias;
        this.num1 = num1;
        this.num2 = num2;
        this.num3 = num3;
        this.num4 = num4;
    }

    public int getOcorrencias() { return ocorrencias; }
    public int getNum1() { return num1; }
    public int getNum2() { return num2; }
    public int getNum3() { return num3; }
    public int getNum4() { return num4; }
    
}
